function buildArticleBody(content: ServiceContent, v: ArticleVars): string {
  const parts = [
    section("Коротко о ситуации", content.intro(v)),
    section("Почему возникает проблема", content.causes(v)),
    listSection(
      "Что можно проверить самостоятельно",
      content.selfCheck(v),
      `Перед вызовом мастера в ${v.cityPrep} можно спокойно выполнить несколько безопасных шагов:`
    ),
    listSection(
      "Когда лучше вызвать мастера",
      content.whenToCall(v),
      `Есть ситуации, где самостоятельные попытки только отнимают время или повышают риск. В ${v.cityPrep} лучше сразу обратиться к специалисту, если:`
    ),
  ];

  return padBody(composeBody(parts), content.extra(v));
}

export function getDzenPhone(serviceSlug: string): string {
  if (isTargetServiceSlug(serviceSlug)) {
    return PHONE_BY_SERVICE[serviceSlug];
  }
  return PHONE_BY_SERVICE.santehnik;
}

export function getTargetCities(pages: Page[]): string[] {
  const filtered = pages.filter((page) => isTargetServiceSlug(page.serviceSlug));
  const citiesInOrder: string[] = [];
  const seen = new Set<string>();

  for (const page of filtered) {
    if (!page.city || seen.has(page.city)) {
      continue;
    }
    seen.add(page.city);
    citiesInOrder.push(page.city);
  }

  const completeCities = citiesInOrder.filter((city) =>
    DZEN_TARGET_SERVICE_SLUGS.every((serviceSlug) =>
      filtered.some((page) => page.city === city && page.serviceSlug === serviceSlug)
    )
  );

  return completeCities.slice(0, DZEN_CITY_LIMIT);
}

export function findPageForCityAndService(
  pages: Page[],
  city: string,
  serviceSlug: DzenTargetServiceSlug
): Page | undefined {
  return pages.find((page) => page.city === city && page.serviceSlug === serviceSlug);
}

export function generateDzenArticle(page: Page, timestamp = nowIso()): DzenArticle {
  if (!isTargetServiceSlug(page.serviceSlug)) {
    throw new Error(`Unsupported service slug for Dzen article: ${page.serviceSlug}`);
  }

  const content = SERVICE_CONTENT[page.serviceSlug];
  const v = getArticleVars(page);
  const body = buildArticleBody(content, v);
  const images = getRandomImages(3);

  return {
    id: `dzen-${page.slug}`,
    title: content.title(v),
    subtitle: content.subtitle(v),
    body,
    cta: buildCta(v),
    tags: content.tags(v),
    targetUrl: v.targetUrl,
    service: page.service,
    city: page.city,
    phone: getDzenPhone(page.serviceSlug),
    slug: page.slug,
    coverImage: images[0] ?? "",
    imageUrls: images,
    status: "draft",
    publishedUrl: null,
    createdAt: timestamp,
    updatedAt: timestamp,
  };
}

export function generateDzenArticlesFromPages(pages: Page[], timestamp = nowIso()): DzenArticle[] {
  const cities = getTargetCities(pages);
  const articles: DzenArticle[] = [];

  for (const city of cities) {
    for (const serviceSlug of DZEN_TARGET_SERVICE_SLUGS) {
      const page = findPageForCityAndService(pages, city, serviceSlug);
      if (!page) {
        continue;
      }
      articles.push(generateDzenArticle(page, timestamp));
    }
  }

  return articles;
}

export function generateDzenArticles(): DzenArticle[] {
  return generateDzenArticlesFromPages(getAllPages());
}
