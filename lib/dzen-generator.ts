function nowIso(): string {
  return new Date().toISOString();
}

function isTargetServiceSlug(value: string): value is DzenTargetServiceSlug {
  return (DZEN_TARGET_SERVICE_SLUGS as readonly string[]).includes(value);
}

function getArticleVars(page: Page): ArticleVars {
  const city = page.city || "";
  const cityPrep = page.cityPrepositional || city;
  const districts = getDistrictsList(page.districts).join(", ");
  const phone = PHONE_BY_SERVICE[page.serviceSlug as DzenTargetServiceSlug] ?? page.phone;

  return {
    service: page.service,
    serviceLower: page.service.toLowerCase(),
    city,
    cityPrep,
    districts,
    phone,
    targetUrl: `${SITE_BASE_URL}/${page.slug}`,
  };
}

function section(title: string, paragraphs: string[]): string {
  const body = paragraphs.filter(Boolean).join("\n\n");
  return `## ${title}\n\n${body}`;
}

function listSection(title: string, items: string[], lead = ""): string {
  const list = items.map((item) => `- ${item}`).join("\n");
  const leadText = lead ? `${lead}\n\n` : "";
  return `## ${title}\n\n${leadText}${list}`;
}

function composeBody(parts: string[]): string {
  return parts.filter(Boolean).join("\n\n");
}

function padBody(body: string, extraParagraphs: string[]): string {
  let result = body;
  let index = 0;

  while (result.length < 2500 && index < extraParagraphs.length) {
    result += `\n\n${extraParagraphs[index]}`;
    index += 1;
  }

  if (result.length > 4000) {
    return `${result.slice(0, 3997).trimEnd()}...`;
  }

  return result;
}

function buildCta(v: ArticleVars): string {
  return `Если нужен мастер в городе ${v.city}, можно оставить заявку здесь: ${v.targetUrl}`;
}

