export function getFailureReasons(page: Page): FailureBlock {
  const serviceSlug = getServiceSlug(page);
  const service = page.service || "техника";
  const items =
    FAILURES[serviceSlug] ??
    (serviceSlug.startsWith("remont-") ? GENERIC_REPAIR : [
      "Не работает как ожидается",
      "Появились посторонние звуки",
      "Снижена эффективность",
      "Требуется профилактика",
      "Нужна замена детали",
      "Срочный выезд",
    ]);

  return {
    title: `Причины поломок: ${service}`,
    items,
  };
}
