export function getExtendedFaqs(page: Page): FaqItem[] {
  const vars = getSeoVars(page);
  const serviceSlug = getServiceSlug(page);

  const specific = SERVICE_SPECIFIC[serviceSlug] ?? [];
  const appliance = APPLIANCE_SLUGS.has(serviceSlug) ? APPLIANCE_FAQS : [];

  const all = [...BASE_FAQS, ...appliance, ...specific];
  const seen = new Set<string>();
  const result: FaqItem[] = [];

  for (const def of all) {
    const item = faqs([def], vars)[0];
    const key = item.question.toLowerCase();
    if (!seen.has(key)) {
      seen.add(key);
      result.push(item);
    }
  }

  return result.slice(0, 35);
}
