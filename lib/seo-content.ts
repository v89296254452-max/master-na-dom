import type { Page } from "./pages";
import { getDistrictsList, getPhone, getServiceSlug } from "./pages";

export interface SeoBlock {
  title: string;
  paragraphs: string[];
  listItems?: string[];
}

interface ContentVars {
  service: string;
  serviceLower: string;
  serviceAcc: string;
  city: string;
  cityPrep: string;
  districts: string;
  phone: string;
}

function vars(page: Page): ContentVars {
  const districts = getDistrictsList(page.districts).join(", ");
  const service = page.service || "Услуга";
  const serviceLower = service.toLowerCase();
  const serviceSlug = getServiceSlug(page);

  const serviceAccMap: Record<string, string> = {
    santehnik: "сантехника",
    elektrik: "электрика",
    "remont-stiralnyh-mashin": "ремонта стиральных машин",
    "remont-holodilnikov": "ремонта холодильников",
    "master-na-chas": "мастера на час",
  };

  return {
    service,
    serviceLower,
    serviceAcc: serviceAccMap[serviceSlug] ?? serviceLower,
    city: page.city || "",
    cityPrep: page.cityPrepositional || page.city || "",
    districts,
    phone: getPhone(page.phone),
  };
}

