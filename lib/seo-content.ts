export function getSeoBlocks(page: Page): SeoBlock[] {
  const v = vars(page);
  const builders = SERVICE_BUILDERS[getServiceSlug(page)] ?? genericBuilders;

  if (!builders) {
    return [];
  }

  return builders.map((build) => build(v));
}

export function getSeoTextLength(page: Page): number {
  return getSeoBlocks(page).reduce((total, block) => {
    const text = [
      block.title,
      ...block.paragraphs,
      ...(block.listItems ?? []),
    ].join(" ");
    return total + text.length;
  }, 0);
}

export function getWhenNeedMasterBlock(page: Page): { title: string; content: string } {
  const blocks = getSeoBlocks(page);
  if (blocks.length === 0) {
    return { title: "", content: "" };
  }

  const firstParagraphs = blocks[0].paragraphs ?? [];
  let text = firstParagraphs.join("\n\n");

  if (text.length < 1000 && blocks[1]) {
    const secondParagraphs = blocks[1].paragraphs ?? [];
    if (secondParagraphs.length) {
      text += `\n\n${secondParagraphs.join("\n\n")}`;
    }
    if (blocks[1].listItems?.length) {
      text += `\n\n${blocks[1].listItems.slice(0, 5).join(". ")}.`;
    }
  }

  if (text.length > 1500) {
    text = `${text.slice(0, 1497).trim()}...`;
  }

  return { title: blocks[0].title, content: text };
}

export function getRemainingSeoBlocks(page: Page): SeoBlock[] {
  const blocks = getSeoBlocks(page);
  return blocks.slice(2);
}
